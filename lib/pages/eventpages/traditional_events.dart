import 'package:app_dtn/pages/eventpages/eventdetail_page.dart';
import 'package:flutter/material.dart';
import 'package:provider/provider.dart';
import 'package:app_dtn/providers/event_provider.dart';

class TraditionalEvents extends StatefulWidget {
  const TraditionalEvents({super.key});

  @override
  State<TraditionalEvents> createState() => _TraditionalEventsState();
}

class _TraditionalEventsState extends State<TraditionalEvents> {
  @override
  Widget build(BuildContext context) {
    return Consumer<EventProvider>(
      builder: (context, eventProvider, child) {
        // L·ªçc c√°c s·ª± ki·ªán "traditional" t·ª´ danh s√°ch s·ª± ki·ªán c·ªßa ng∆∞·ªùi d√πng
        final traditionalEvents =
            eventProvider.myEvents
                .where(
                  (event) => event.eventType.toLowerCase() == 'traditional',
                )
                .toList();

        debugPrint(
          'üì± TraditionalEvents: C√≥ ${traditionalEvents.length} s·ª± ki·ªán truy·ªÅn th·ªëng',
        );

        if (traditionalEvents.isEmpty) {
          return const Center(
            child: Column(
              mainAxisAlignment: MainAxisAlignment.center,
              children: [
                Icon(Icons.local_fire_department, size: 60, color: Colors.grey),
                SizedBox(height: 16),
                Text(
                  "B·∫°n ch∆∞a tham gia s·ª± ki·ªán truy·ªÅn th·ªëng n√†o",
                  textAlign: TextAlign.center,
                ),
              ],
            ),
          );
        }

        return ListView.builder(
          itemCount: traditionalEvents.length,
          itemBuilder: (context, index) {
            final event = traditionalEvents[index];
            return Card(
              margin: const EdgeInsets.all(10),
              child: ListTile(
                leading: Icon(Icons.local_fire_department, color: Colors.red),
                title: Text(
                  event.name,
                  style: TextStyle(fontWeight: FontWeight.bold),
                ),
                subtitle: Text(
                  event.description,
                  maxLines: 2,
                  overflow: TextOverflow.ellipsis,
                ),
                trailing: Icon(
                  Icons.arrow_forward_ios,
                  size: 16,
                  color: Colors.grey,
                ),
                onTap: () {
                  debugPrint(
                    'üì± TraditionalEvents: ƒê√£ ch·ªçn s·ª± ki·ªán ID=${event.id}',
                  );

                  // Ch·ªâ ƒë·∫∑t ID, kh√¥ng g·ªçi fetchEventDetails
                  Provider.of<EventProvider>(
                    context,
                    listen: false,
                  ).setSelectedEventId(event.id);

                  // ƒêi·ªÅu h∆∞·ªõng ƒë·∫øn trang chi ti·∫øt (trang n√†y s·∫Ω t·ª± g·ªçi fetchEventDetails trong initState)
                  Navigator.push(
                    context,
                    MaterialPageRoute(builder: (context) => EventDetailPage()),
                  );
                },
              ),
            );
          },
        );
      },
    );
  }
}
