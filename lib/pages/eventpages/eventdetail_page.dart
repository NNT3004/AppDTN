        ScaffoldMessenger.of(context).showSnackBar(
          const SnackBar(
            content: Text('Kh√¥ng t√¨m th·∫•y th√¥ng tin s·ª± ki·ªán'),
            backgroundColor: Colors.orange,
          ),
