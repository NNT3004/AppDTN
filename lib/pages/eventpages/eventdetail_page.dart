        debugPrint(
          '‚ùå EventDetailPage: ƒêƒÉng k√Ω s·ª± ki·ªán th·∫•t b·∫°i: ${eventProvider.error}',
        );
        if (mounted) {
          ScaffoldMessenger.of(context).showSnackBar(
            SnackBar(
              content: Text(
                'ƒêƒÉng k√Ω th·∫•t b·∫°i: ${eventProvider.error ?? "Kh√¥ng x√°c ƒë·ªãnh"}',
              ),
              backgroundColor: Colors.red,
            ),
          );
        }
      }
    } catch (e) {
      debugPrint('‚ùå EventDetailPage: L·ªói khi ƒëƒÉng k√Ω s·ª± ki·ªán: $e');
      if (mounted) {
        ScaffoldMessenger.of(context).showSnackBar(
          SnackBar(
            content: Text('ƒê√£ x·∫£y ra l·ªói: $e'),
            backgroundColor: Colors.red,
          ),
        );
      }
    } finally {
      if (mounted) {
        setState(() {
          _isRegistering = false;
        });
      }
    }
  }

  // Widget hi·ªÉn th·ªã th√¥ng tin s·ª± ki·ªán
  Widget _buildEventInfo(String label, String value) {
    return Padding(
      padding: const EdgeInsets.symmetric(vertical: 4),
      child: Row(
        crossAxisAlignment: CrossAxisAlignment.start,
        children: [
          SizedBox(
            width: 120,
            child: Text(
              label,
              style: const TextStyle(
                fontWeight: FontWeight.bold,
                color: Colors.black87,
              ),
            ),
          ),
          Expanded(
            child: Text(value, style: const TextStyle(color: Colors.black87)),
          ),
        ],
      ),
    );
  }

  // Ki·ªÉm tra xem ng∆∞·ªùi d√πng ƒë√£ ƒëƒÉng k√Ω s·ª± ki·ªán ch∆∞a
  bool _isAlreadyRegistered(Event event, List<Event> myEvents) {
    return myEvents.any((myEvent) => myEvent.id == event.id);
  }

  // Ki·ªÉm tra xem c√≥ th·ªÉ ƒëƒÉng k√Ω s·ª± ki·ªán hay kh√¥ng
  bool _canRegister(Event event) {
    final now = DateTime.now();
    return now.isAfter(event.registrationStartDate) &&
        now.isBefore(event.registrationEndDate) &&
        event.currentRegistrations < event.maxRegistrations;
  }

  @override
  Widget build(BuildContext context) {
    return Consumer<EventProvider>(
      builder: (context, eventProvider, child) {
        final event = eventProvider.selectedEvent;

        // Kh√¥ng g·ªçi API n√†o ·ªü ƒë√¢y, ch·ªâ s·ª≠ d·ª•ng d·ªØ li·ªáu c√≥ s·∫µn
        // N·∫øu c·∫ßn l√†m m·ªõi d·ªØ li·ªáu, s·ª≠ d·ª•ng ElevatedButton v·ªõi onPressed
        // ho·∫∑c c√°c t∆∞∆°ng t√°c ng∆∞·ªùi d√πng kh√°c

        // Hi·ªÉn th·ªã loading khi ƒëang t·∫£i chi ti·∫øt s·ª± ki·ªán
        if (eventProvider.isLoading) {
          debugPrint('üì± EventDetailPage: ƒêang t·∫£i chi ti·∫øt s·ª± ki·ªán...');
          return const Center(child: CircularProgressIndicator());
