      final result = await eventProvider.registerEvent(eventId);

      if (result) {
        debugPrint('‚úÖ EventDetailPage: ƒêƒÉng k√Ω s·ª± ki·ªán th√†nh c√¥ng');

        // C·∫≠p nh·∫≠t l·∫°i danh s√°ch s·ª± ki·ªán c·ªßa t√¥i
        await eventProvider.fetchMyEvents(refresh: true);

