                            ),
                          ),
                ),
              ),
            ],
          ),
        );
      },
    );
  }

  // H√†m l·∫•y m√†u d·ª±a v√†o lo·∫°i s·ª± ki·ªán
  Color _getEventColor(String eventType) {
    switch (eventType.toLowerCase()) {
      case 'traditional':
        return Colors.red;
      case 'academic':
        return Colors.green;
      case 'union':
        return Colors.blue;
      default:
        return Colors.grey;
    }
  }
}

void main() {
  runApp(
    MaterialApp(home: EventDetailPage(), debugShowCheckedModeBanner: false),
  );
}
