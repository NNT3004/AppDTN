  // H√†m x·ª≠ l√Ω khi ng∆∞·ªùi d√πng ƒëƒÉng k√Ω tham gia s·ª± ki·ªán
  Future<void> _registerEvent(int eventId) async {
    debugPrint('üì± EventDetailPage: ƒêang ƒëƒÉng k√Ω s·ª± ki·ªán ID=$eventId');

    if (_isRegistering) {
