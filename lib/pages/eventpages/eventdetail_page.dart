          return const Center(child: Text('Kh√¥ng t√¨m th·∫•y th√¥ng tin s·ª± ki·ªán'));
        }

        // Ki·ªÉm tra xem ng∆∞·ªùi d√πng ƒë√£ ƒëƒÉng k√Ω ch∆∞a
        final isRegistered = _isAlreadyRegistered(
          event,
          eventProvider.myEvents,
        );
        final canRegister = _canRegister(event);

        debugPrint(
          'üì± EventDetailPage: Hi·ªÉn th·ªã s·ª± ki·ªán ID=${event.id}, t√™n=${event.name}',
        );
        debugPrint(
          'üì± EventDetailPage: ƒê√£ ƒëƒÉng k√Ω: $isRegistered, C√≥ th·ªÉ ƒëƒÉng k√Ω: $canRegister',
        );

        // Format ng√†y gi·ªù
        final dateFormat = DateFormat('dd/MM/yyyy HH:mm');

        return SingleChildScrollView(
          padding: const EdgeInsets.all(16),
          child: Column(
            crossAxisAlignment: CrossAxisAlignment.start,
            children: [
              // Ti·∫øn tr√¨nh ƒëƒÉng k√Ω
              Card(
                elevation: 2,
                child: Padding(
                  padding: const EdgeInsets.all(16),
                  child: Column(
                    crossAxisAlignment: CrossAxisAlignment.start,
                    children: [
                      Row(
                        mainAxisAlignment: MainAxisAlignment.spaceBetween,
                        children: [
                          Text(
                            "Ti·∫øn tr√¨nh ƒëƒÉng k√Ω",
                            style: TextStyle(
                              fontSize: 16,
                              fontWeight: FontWeight.bold,
                              color: Colors.blue[900],
                            ),
                          ),
                          Text(
                            "${event.currentRegistrations}/${event.maxRegistrations}",
                            style: const TextStyle(fontWeight: FontWeight.bold),
                          ),
                        ],
                      ),
                      const SizedBox(height: 8),
                      LinearProgressIndicator(
                        value:
                            event.maxRegistrations > 0
                                ? event.currentRegistrations /
                                    event.maxRegistrations
                                : 0,
                        backgroundColor: Colors.grey[200],
                        valueColor: AlwaysStoppedAnimation<Color>(
                          Colors.blue[700]!,
                        ),
                        minHeight: 10,
                        borderRadius: BorderRadius.circular(5),
                      ),
                    ],
                  ),
                ),
              ),

              const SizedBox(height: 16),

              // Th√¥ng tin ch√≠nh c·ªßa s·ª± ki·ªán
              Card(
                elevation: 2,
                child: Padding(
                  padding: const EdgeInsets.all(16),
                  child: Column(
                    crossAxisAlignment: CrossAxisAlignment.start,
                    children: [
                      Row(
                        children: [
                          Icon(
                            event.icon,
                            color: _getEventColor(event.eventType),
                            size: 20,
                          ),
                          const SizedBox(width: 8),
                          Text(
                            event.eventType,
                            style: TextStyle(
                              color: _getEventColor(event.eventType),
                              fontWeight: FontWeight.bold,
                            ),
                          ),
                        ],
                      ),
                      const SizedBox(height: 8),
                      Text(
                        event.name,
                        style: const TextStyle(
                          fontSize: 20,
                          fontWeight: FontWeight.bold,
                        ),
                      ),
                      const SizedBox(height: 8),
                      Text(
                        event.description,
                        style: TextStyle(color: Colors.grey[700]),
                      ),
                    ],
                  ),
                ),
              ),

              const SizedBox(height: 16),

              // Chi ti·∫øt s·ª± ki·ªán
              Card(
                elevation: 2,
                child: Padding(
                  padding: const EdgeInsets.all(16),
                  child: Column(
                    crossAxisAlignment: CrossAxisAlignment.start,
                    children: [
                      Text(
                        "Th√¥ng tin chi ti·∫øt",
                        style: TextStyle(
                          fontSize: 16,
                          fontWeight: FontWeight.bold,
                          color: Colors.blue[900],
                        ),
                      ),
                      const SizedBox(height: 16),
                      _buildEventInfo("ƒê·ªãa ƒëi·ªÉm:", event.location),
                      _buildEventInfo("ƒêi·ªÉm:", "${event.score} ƒëi·ªÉm"),
                      _buildEventInfo(
                        "Th·ªùi gian:",
                        "${dateFormat.format(event.startDate)} - ${dateFormat.format(event.endDate)}",
                      ),
                      _buildEventInfo(
                        "Th·ªùi gian ƒëƒÉng k√Ω:",
                        "${dateFormat.format(event.registrationStartDate)} - ${dateFormat.format(event.registrationEndDate)}",
                      ),
                      _buildEventInfo("Tr·∫°ng th√°i:", event.status),
                    ],
                  ),
                ),
              ),

              const SizedBox(height: 16),

              // H√¨nh ·∫£nh s·ª± ki·ªán
              if (event.eventImages.isNotEmpty)
                Card(
                  elevation: 2,
                  child: Padding(
                    padding: const EdgeInsets.all(16),
                    child: Column(
                      crossAxisAlignment: CrossAxisAlignment.start,
                      children: [
                        Text(
                          "H√¨nh ·∫£nh s·ª± ki·ªán",
                          style: TextStyle(
                            fontSize: 16,
                            fontWeight: FontWeight.bold,
                            color: Colors.blue[900],
                          ),
                        ),
                        const SizedBox(height: 16),
                        SizedBox(
                          height: 200,
                          child: ListView.builder(
                            scrollDirection: Axis.horizontal,
                            itemCount: event.eventImages.length,
                            itemBuilder: (context, index) {
                              return Padding(
                                padding: const EdgeInsets.only(right: 8),
                                child: ClipRRect(
                                  borderRadius: BorderRadius.circular(8),
                                  child: Image.network(
                                    event.eventImages[index].imageUrl,
                                    width: 300,
